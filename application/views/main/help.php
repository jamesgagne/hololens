<h3 class="h3">How to Register</h3>

<strong>Step 1.</strong> Click on the "Log In" dropdown at the top-right of the screen.<br />

<strong>Step 2.</strong> Click on "Create Account?" to navigate to the register page.<br />

<strong>Step 3.</strong> Fill in the form and click "Register" when done.<br />

<strong>Step 4.</strong> Login!


<h3 class="h3">How to Login</h3>

<strong>Step 1.</strong> Click on the "Log In" dropdown at the top-right of the screen.<br />

<strong>Step 2.</strong> Enter your email and password.<br />

<strong>Step 3.</strong> Click the "Log In" button OR press enter


<h3 class="h3">How to reset your password</h3>

<strong>Step 1.</strong> Click on the "Log In" dropdown at the top-right of the screen.<br />

<strong>Step 2.</strong> Click on "Forgot Password?" to navigate to the password reset page.<br />

<strong>Step 3.</strong> Enter your email in the field and click the "Next" button OR press enter.<br />

<strong>Step 4.</strong> Enter the answer to your security question into the field and click "Next" or press enter.<br />

<strong>Step 5.</strong> Enter your new password into the "New Password" and "Confirm Password" fields and click "Change Password" or press enter.

<h3 class="h3">How to Use the Catalog</h3>

 <p>The catalog page displays the fbx files currently uploaded in the database. It is available to non logged in users who can download from the database if they want.
Additional functionality exists for members who are logged in and for administrators. Members who are logged in can add files selected on the catalog page to their own
"Workspace" which is a user named list that contains the specific models the user has added. Administrators also have the option to delete a model from the
database.</p>

<h4 class="h4">How to Download</h4>

<strong>Step 1.</strong> Select the models you wish to download.<br />

<strong>Step 2.</strong> Click the download button.<br />

<br>

<h4 class="h4">How to Add to Workspace</h4>

<strong>Step 1.</strong> Click the models you wish to add to a workspace.<br />

<strong>Step 2.</strong> Click the add to workspace button.<br />

<strong>Step 3.</strong> Select the workspace from the list to add the files to and click the OK button.<br />

<br>

<h4 class="h4">How to Delete (Admin Only)</h4>

<strong>Step 1.</strong> Click the models you wish to delete.<br />

<strong>Step 2.</strong> Click the delete button.<br />

<strong>Step 3.</strong> Click the OK button to confirm.<br />

<br>

<h4 class="h4">How to Filter and Search Models</h4>

<p>The catalog can be filtered to assist the user in finding the desired model file. You can filter models by their assigned category and also by color. 
Additionally the search bar at the top-right of the page can be used at any time to search for models by name and you will be brought to the catalog page
with the search results. These results can then be further filtered by using the category and color filters to the left.</p>

<h3 class="h3">How to Upload</h3>

<strong>Step 1.</strong> Click on the Upload tab at the top of the page.<br />

<strong>Step 2.</strong> You can either drag and drop your files onto the designated zone or click it to open the file selection. Once you have selected your files you can click OK to close the file selection.<br />

<strong>Step 3.</strong> Once the file selection window is closed, you will be able to set categories and colors for the files individually. You will also be able to upload a screen shot and write a brief description for each file at this time.<br />

<strong>Step 4.</strong> Once you are done, you can upload the files by clicking the upload button. A success message should appear when the upload is done.

<h3 class="h3">How to Use the Administration Page (Admin Only)</h3>

<p>The administraton page can be used to manage users, colors, and categories. To add a new user click the "Add User" button and complete the form and finally press OK to create the new user.
To add a new color or category click either "Add Color" or "Add Category" button and enter the desired name into the field and press OK to add. </p>

<p>Existing colors, and categories can be edited by clicking
the pencil icon next to the record in the table. To delete a record click the trash can icon next to its record in the table.</p>


<h3 class="h3">How to Use the Profile</h3>

<strong>Step 1.</strong> Navigate to the Profile page by clicking the user dropdown at the top-right and clicking the "My Profile" button.<br /> 

<strong>Step 2.</strong> Enter your new values into the fields and click the "Save Profile Details" button.<br />


<h3 class="h3">How to Use Workspaces</h3>

<p>Navigate to the Workspace page by clicking on the user dropdown at the top-right of the screen and clicking the "My Workspaces" button.
Once here you can change the "Current Workspace" by clicking on the dropdown and changing the selection. When selected the page will reload and display
the models in that workspace. Additonally you can delete and add Workspaces from the page. The delete button will delete the current workspace.
You can also remove models from the list here by clicking the "Remove From Workspace" button. You can also download the files here similar to the catalog.</p>